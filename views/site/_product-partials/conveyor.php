<p><strong>Конвейер цепной напольный</strong> — нашел широкое применение в различных отраслях промышленности.</p>

<p>Принцип работы напольного цепного конвейера основан на перемещении грузов с помощью привода тяговой грузонесущей цепи, расположенной в направляющих. В состав напольного конвейера, как и подвесного входят:</p>

<ol start="1"><li>Направляющие, закрепленные в полу таким образом, чтобы пластины, удерживающие груз, находились заподлицо с полом.</li>
    <li>Тяговая цепь с пластинами («тарелками»).</li>
    <li>Станция приводная в сборе.</li>
    <li>Станция натяжная в сборе.</li>
    <li>Станция поворотная.</li>
    <li>Пост обслуживания и ремонта, смазки тяговой цепи и пластин.</li>
</ol><p>При заказе напольного конвейера компанией проводится проектное сопровождение (выезд на предприятие, обмер, составление рабочего проекта, согласованное заказчиком и надзорными организациями). По согласованию с сторонами проводятся монтажные работы и запуск оборудования.</p>


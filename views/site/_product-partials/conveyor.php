<p><strong>Винтовые (шнековые) конвейеры</strong> предназначены для перемещения в горизонтальном или наклонном направлениях сыпучих, пылевидных грузов.</p>

<p>Основные преимущества применения винтовых конвейеров:</p>
<p>1. Простая конструкция винта в виде спирали обеспечивает эффективное перемещение материала.</p>
<p>2. Удобство монтажа.</p>
<p>3. Возможность установки конвейера винтового под углом позволяет экономить производственные площади.</p>

<p><strong>Винтовой конвейер</strong> состоит из следующих сборочных единиц и деталей: корпуса, винта, подшипниковых опор, загрузочного и разгрузочного отверстия, редуктора и приводного электродвигателя.</p>

<p><strong>Винтовой конвейер</strong> имеет стандартный ряд диаметров – 120, 160 ,200, 250, 320, 400, 500мм.</p>
<p>В зависимости от условий установки винтового конвейера в составе технологических линий привод винта может быть выполнен как по толкающему так и по тянущему исполнению.</p>

